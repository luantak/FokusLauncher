        "Share",
        "MusicNote",
        "Videocam",
        "CameraAlt",
        "Photo",
        "Image",
        "PlayArrow",
        "Palette",
        "Book",
        "Newspaper",
        "Headset",
        "Home",
        "Map",
        "Place",
        "LocationOn",
